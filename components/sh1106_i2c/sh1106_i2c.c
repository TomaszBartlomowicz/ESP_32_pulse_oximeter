static esp_err_t sh1106_write_byte(uint8_t control_byte, uint8_t data) {
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (SH1106_I2C_ADDRESS << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd, control_byte, true);
    i2c_master_write_byte(cmd, data, true);
    i2c_master_stop(cmd);
    esp_err_t ret = i2c_master_cmd_begin(I2C_MASTER_NUM, cmd, pdMS_TO_TICKS(I2C_TIMEOUT_MS));
    i2c_cmd_link_delete(cmd);
    return ret;
}

void sh1106_send_command(uint8_t cmd) {
    sh1106_write_byte(SH1106_COMMAND_MODE, cmd);
}

void sh1106_send_data(uint8_t data) {
    sh1106_write_byte(SH1106_DATA_MODE, data);
}

void sh1106_init(void) {
    i2c_config_t conf = {
        .mode = I2C_MODE_MASTER,
        .sda_io_num = I2C_MASTER_SDA_IO,
        .scl_io_num = I2C_MASTER_SCL_IO,
        .sda_pullup_en = GPIO_PULLUP_ENABLE,
        .scl_pullup_en = GPIO_PULLUP_ENABLE,
        .master.clk_speed = I2C_MASTER_FREQ_HZ
    };
    i2c_param_config(I2C_MASTER_NUM, &conf);
    i2c_driver_install(I2C_MASTER_NUM, conf.mode,
                       I2C_MASTER_RX_BUF_DISABLE,
                       I2C_MASTER_TX_BUF_DISABLE, 0);

    vTaskDelay(pdMS_TO_TICKS(100));

    sh1106_send_command(0xAE); // display off
    sh1106_send_command(0xD5);
    sh1106_send_command(0x80);
    sh1106_send_command(0xA8);
    sh1106_send_command(0x3F);
    sh1106_send_command(0xD3);
    sh1106_send_command(0x00);
    sh1106_send_command(0x40);
    sh1106_send_command(0xAD);
    sh1106_send_command(0x8B);
    sh1106_send_command(0xA1);
    sh1106_send_command(0xC8);
    sh1106_send_command(0xDA);
    sh1106_send_command(0x12);
    sh1106_send_command(0x81);
    sh1106_send_command(0xCF);
    sh1106_send_command(0xD9);
    sh1106_send_command(0xF1);
    sh1106_send_command(0xDB);
    sh1106_send_command(0x40);
    sh1106_send_command(0xA4);
    sh1106_send_command(0xA6);
    sh1106_send_command(0xAF); // display on
}

void sh1106_clear(void) {
    memset(sh1106_buffer, 0x00, sizeof(sh1106_buffer));
}


void sh1106_display(void) {
    for (uint8_t page = 0; page < SH1106_PAGE_COUNT; page++) {
        sh1106_send_command(0xB0 + page); // set page address
        sh1106_send_command(0x02);        // set lower column address
        sh1106_send_command(0x10);        // set higher column address

        for (uint8_t col = 0; col < SH1106_WIDTH; col++) {
            sh1106_send_data(sh1106_buffer[page * SH1106_WIDTH + col]);
        }
    }
}

